package numberEx2;

public class FourArithemticEx2 {

	public static void main(String[] args) {
		int i = 1;
		int j = 10;
//		i++; // ++는 값 1만큼 증가 시킨다
//		j--; // --는 값을 1만큼 감소 시킨다
		
		System.out.println(i);
		System.out.println(i++);// i++ 값이 참조된후 증가
		System.out.println(++i); // ++i 값이 참조되기전에 증가
		System.out.println(j);
	}

}
